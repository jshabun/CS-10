/*
Directions:
Write a program whose input is a character and a string, and whose output indicates the number of times the character appears in the string.

Ex: If the input is: n Monday, the output is:
1

Ex: If the input is: z Today is Monday, the output is:
0

Ex: If the input is: n It's a sunny day, the output is:
2

Case matters. n is different than N.

Ex: If the input is: n Nobody, the output is:
0

Your program must define and call the following function that returns the number of times the input character appears in the input string. 
	int CountCharacters(char userChar, string userString)

Note: This is a lab from a previous chapter that now requires the use of a function.
*/

#include <iostream>
using namespace std;

int CountCharacters (char userChar, string userString) {
   int charCount = 0;
   
   for (unsigned int i = 0; i < userString.length(); ++i) {
      if (userChar == userString[i]) {
         charCount++;
      }
   }
   return charCount;
}

int main() {
   char characterInput;
   string stringInput;
   
   cin >> characterInput;
   getline (cin, stringInput);
   
   cout << CountCharacters (characterInput, stringInput) << endl;

   return 0;
}
